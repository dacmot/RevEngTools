package recurrence;

public class ArrayAssignmentException extends Exception
{

    public ArrayAssignmentException()
    {
        super();
    }

    public ArrayAssignmentException(String message)
    {
        super(message);
    }

    public ArrayAssignmentException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public ArrayAssignmentException(Throwable cause)
    {
        super(cause);
    }

}
