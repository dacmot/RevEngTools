package recurrence;

public class UnknownSignLoopIncrException extends Exception
{

    public UnknownSignLoopIncrException()
    {
        super();
    }

    public UnknownSignLoopIncrException(String message)
    {
        super(message);
    }

    public UnknownSignLoopIncrException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public UnknownSignLoopIncrException(Throwable cause)
    {
        super(cause);
    }

}
